package com.didispace.entity;

public class TitleEntity {

    private String numberTitle;

    private String dateTitle;

    private String loanMoneyTitle;

    public String getNumberTitle() {
        return numberTitle;
    }

    public void setNumberTitle(String numberTitle) {
        this.numberTitle = numberTitle;
    }

    public String getDateTitle() {
        return dateTitle;
    }

    public void setDateTitle(String dateTitle) {
        this.dateTitle = dateTitle;
    }

    public String getLoanMoneyTitle() {
        return loanMoneyTitle;
    }

    public void setLoanMoneyTitle(String loanMoneyTitle) {
        this.loanMoneyTitle = loanMoneyTitle;
    }
}
